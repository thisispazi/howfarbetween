export function getNearbyCities(city: City, count: number = 6): City[] {
  return CITIES
    .filter(c => c.slug !== city.slug)
    .map(c => ({
      city: c,
      distance: getDistanceKm(city.lat, city.lon, c.lat, c.lon)
    }))
    .sort((a, b) => a.distance - b.distance)
    .slice(0, count)
    .map(item => item.city)
}